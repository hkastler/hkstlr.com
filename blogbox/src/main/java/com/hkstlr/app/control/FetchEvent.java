package com.hkstlr.app.control;

public class FetchEvent {
	
	private String event = "";
	
	public FetchEvent() {
		super();
	}

	public FetchEvent(String event) {
		super();
		this.event = event;
	}

	public String getEvent() {
		return event;
	}

	public void setEvent(String event) {
		this.event = event;
	}
	
	
	
	

}
